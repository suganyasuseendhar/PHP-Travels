package inheritance;

public class Calculator {

	
		public int add(int a, int b) {

			return (a + b);
			

		}

		public int add(int a, int b, int c) {

			return (a + b + c);

		}

		private int add(int a, int b, int c, int d) {

			return (a + b + c + d);

		}

		public int add(int a, int b, int c, int d, int e) {

			return (a + b + c + d + e);

		}

		public int sub(int a, int b) {

			return (a - b);

		}

		public int sub(int a, int b, int c) {

			return (a - b - c);

		}

		public int sub(int a, int b, int c, int d) {

			return (a - b - c - d);

		}

		public int sub(int a, int b, int c, int d, int e) {

			return (a - b - c - d - e);

		}

		private int mul(int a, int b) {

			return (a * b);

		}

		public int mul(int a, int b, int c) {

			return (a * b * c);

		}

		public int mul(int a, int b, int c, int d) {

			return (a * b * c * d);

		}

		public int mul(int a, int b, int c, int d, int e) {

			return (a * b * c * d * e);

		}

		public int div(int a, int b) {

			return (a / b);

		}

		public int div(int a, int b, int c) {

			return (a / b / c);

		}

		public int div(int a, int b, int c, int d) {

			return (a / b / c / d);

		}

		public int div(int a, int b, int c, int d, int e) {

			return (a / b / c / d / e);

		}

	}

